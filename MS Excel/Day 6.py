# ----------------------------------------------------------
# 1. What is a Pivot Table?
# ----------------------------------------------------------
# A Pivot Table is a tool in Excel used to:
# - Summarize data
# - Analyze data
# - Organize large datasets
# - Create reports easily
#
# Pivot Table helps convert raw data into useful information.

# Example Raw Data:
# Name     City     Sales
# Vatsal   Delhi    500
# Rahul    Mumbai   700
# Vatsal   Delhi    300

# Pivot Table can show:
# Total Sales by City
# Total Sales by Person

# ----------------------------------------------------------
# 2. Why Pivot Tables are Important?
# ----------------------------------------------------------
# Benefits:
# - Fast data analysis
# - No formulas required
# - Easy report creation
# - Used in dashboards
# - Used by Data Analysts

# ----------------------------------------------------------
# 3. Creating a Pivot Table
# ----------------------------------------------------------
# Steps:
# 1. Select data
# 2. Go to Insert tab
# 3. Click Pivot Table
# 4. Select Table/Range
# 5. Click OK

# Pivot Table will be created.

# ----------------------------------------------------------
# 4. Pivot Table Fields
# ----------------------------------------------------------
# Pivot Table has 4 main areas:

# 1. Rows
# Shows row categories

# Example:
# Customer Name

# 2. Columns
# Shows column categories

# Example:
# City

# 3. Values
# Shows calculations

# Example:
# Sum of Sales

# 4. Filters
# Used to filter data

# Example:
# Show only Delhi data

# ----------------------------------------------------------
# 5. Example of Pivot Table Analysis
# ----------------------------------------------------------

# Raw Data:
# Name     City     Sales
# A        Delhi    100
# B        Mumbai   200
# A        Delhi    300

# Pivot Table Result:
# Name     Total Sales
# A        400
# B        200

# ----------------------------------------------------------
# 6. Changing Calculation Type
# ----------------------------------------------------------
# Pivot Table supports different calculations:

# Sum
# Count
# Average
# Max
# Min

# Example:
# Show Average Sales instead of Sum

# Steps:
# Right click → Value Field Settings → Choose function

# ----------------------------------------------------------
# 7. Filtering in Pivot Table
# ----------------------------------------------------------
# Used to show specific data.

# Example:
# Show only Delhi sales

# Steps:
# Drag City into Filter area

# ----------------------------------------------------------
# 8. Sorting Pivot Table
# ----------------------------------------------------------
# Arrange data in order.

# Example:
# Highest sales to lowest sales

# Steps:
# Right click → Sort → Largest to Smallest

# ----------------------------------------------------------
# 9. Pivot Charts (Visualization)
# ----------------------------------------------------------
# Pivot Charts help visualize Pivot Table data.

# Types of charts:
# - Bar chart
# - Column chart
# - Pie chart
# - Line chart

# Steps:
# Select Pivot Table → Insert → Pivot Chart

# ----------------------------------------------------------
# 10. Refreshing Pivot Table
# ----------------------------------------------------------
# If original data changes,
# Pivot Table must be refreshed.

# Steps:
# Right click → Refresh

# ----------------------------------------------------------
# 11. Grouping Data
# ----------------------------------------------------------
# Used to group data.

# Example:
# Group dates by Month
# Group ages into ranges

# Steps:
# Right click → Group

