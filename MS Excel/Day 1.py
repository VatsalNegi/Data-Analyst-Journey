# --------------------------------------------
# 1. What is MS Excel?
# --------------------------------------------
# MS Excel is a spreadsheet software developed by Microsoft.
# It is used to store, organize, analyze, and visualize data.
# Excel is widely used in finance, data analysis, HR, marketing,
# education, and many other fields.

# --------------------------------------------
# 2. Workbook and Worksheet
# --------------------------------------------
# Workbook:
# A workbook is an Excel file.
# Example: data.xlsx

# Worksheet:
# A worksheet is a single sheet inside a workbook.
# One workbook can contain multiple worksheets.

# --------------------------------------------
# 3. Rows, Columns, and Cells
# --------------------------------------------
# Row:
# Horizontal lines in Excel (1, 2, 3, ...)

# Column:
# Vertical lines in Excel (A, B, C, ...)

# Cell:
# Intersection of a row and a column.
# Example: A1, B2, C5

# --------------------------------------------
# 4. Cell Address / Cell Reference
# --------------------------------------------
# Cell address tells the exact location of a cell.
# Example:
# A1 -> Column A, Row 1
# D5 -> Column D, Row 5

# --------------------------------------------
# 5. Data Types in Excel
# --------------------------------------------
# Excel supports different types of data:

# 1. Text (Labels)
# Example: Name, City, Product

# 2. Numbers (Values)
# Example: 10, 25.5, 1000

# 3. Date and Time
# Example: 01-01-2025, 10:30 AM

# 4. Boolean
# TRUE or FALSE

# --------------------------------------------
# 6. Entering Data in Excel
# --------------------------------------------
# To enter data:
# 1. Click on a cell
# 2. Type the data
# 3. Press Enter

# --------------------------------------------
# 7. Basic Excel Operations
# --------------------------------------------
# Copy:
# Ctrl + C

# Paste:
# Ctrl + V

# Cut:
# Ctrl + X

# Undo:
# Ctrl + Z

# Redo:
# Ctrl + Y

# --------------------------------------------
# 8. Saving an Excel File
# --------------------------------------------
# Steps:
# 1. Click File
# 2. Click Save or Save As
# 3. Choose location
# 4. Enter file name
# 5. Click Save

# --------------------------------------------
# 9. Basic Formulas in Excel
# --------------------------------------------
# Formulas always start with '=' sign

# Example formulas:
# =A1 + A2       -> Addition
# =A1 - A2       -> Subtraction
# =A1 * A2       -> Multiplication
# =A1 / A2       -> Division

# --------------------------------------------
# 10. Common Excel Functions
# --------------------------------------------

# SUM:
# Adds numbers
# Example:
# =SUM(A1:A5)

# AVERAGE:
# Finds average value
# Example:
# =AVERAGE(A1:A5)

# MIN:
# Finds minimum value
# Example:
# =MIN(A1:A5)

# MAX:
# Finds maximum value
# Example:
# =MAX(A1:A5)

# COUNT:
# Counts numeric values
# Example:
# =COUNT(A1:A5)

# --------------------------------------------
# 11. AutoFill Feature
# --------------------------------------------
# AutoFill is used to automatically fill data.
# Example:
# If you type 1 and 2, then drag down,
# Excel will automatically fill 3, 4, 5, ...

# --------------------------------------------
# 12. Formatting Cells
# --------------------------------------------
# Formatting helps improve readability.

# Common formatting options:
# - Font size
# - Font color
# - Bold, Italic, Underline
# - Cell background color
# - Alignment (Left, Center, Right)

# --------------------------------------------
# 13. Basic Excel Use Cases
# --------------------------------------------
# - Student marksheets
# - Attendance sheets
# - Sales reports
# - Expense tracking
# - Data analysis

