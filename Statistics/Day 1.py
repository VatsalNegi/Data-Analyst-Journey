# 1. STATISTICS AND ITS APPLICATIONS
# =====================================================

"""
Statistics is the science of collecting, organizing,
analyzing, and interpreting data to make decisions.

Why Statistics is Important:
- Helps understand large datasets
- Identifies patterns and trends
- Supports decision-making
- Forms the foundation of Data Science and ML

Applications of Statistics:
- Business: Sales analysis, market research
- Healthcare: Disease analysis, clinical trials
- Finance: Risk and investment analysis
- Government: Census and surveys
- Data Science: EDA, model evaluation
"""

# =====================================================
# 2. POPULATION VS SAMPLE
# =====================================================

"""
Population:
The complete set of all elements under study.

Example:
All customers of an e-commerce platform.

Sample:
A subset selected from the population.

Example:
1000 customers selected for analysis.

Why use samples?
- Population is too large
- Saves time and cost
- Faster analysis
"""

# =====================================================
# 3. POPULATION VS SAMPLE DISTRIBUTION
# =====================================================

"""
Population Distribution:
- Describes the entire population
- Mean is denoted by μ (mu)
- Standard Deviation is denoted by σ (sigma)

Sample Distribution:
- Describes sample data
- Mean is denoted by x̄ (x-bar)
- Standard Deviation is denoted by s

Sample statistics are used to estimate
population parameters.
"""

# =====================================================
# 4. TYPES OF SAMPLING
# =====================================================

"""
Sampling is the process of selecting a subset
from a population for analysis.
"""

# -------------------------------
# A. PROBABILITY SAMPLING
# -------------------------------

"""
In probability sampling, every element
has a known and equal chance of selection.
"""

# 1. Simple Random Sampling
"""
- Every element has equal probability
- Lottery method

Example:
Randomly selecting 100 students from a university
"""

# 2. Systematic Sampling
"""
- Select every k-th element

Example:
Selecting every 10th customer from a list
"""

# 3. Stratified Sampling
"""
- Population divided into strata (groups)
- Samples taken from each group

Example:
Sampling students from each department
"""

# 4. Cluster Sampling
"""
- Population divided into clusters
- Entire cluster selected

Example:
Selecting one city and surveying all people
"""

# -------------------------------
# B. NON-PROBABILITY SAMPLING
# -------------------------------

"""
In non-probability sampling, not every
element has an equal chance of selection.
"""

# 1. Convenience Sampling
"""
- Based on ease of access

Example:
Surveying friends or nearby people
"""

# 2. Judgmental (Purposive) Sampling
"""
- Researcher selects samples using judgment

Example:
Interviewing industry experts
"""

# 3. Quota Sampling
"""
- Fixed quota for categories

Example:
50 males and 50 females
"""

# 4. Snowball Sampling
"""
- Existing participants recruit new participants

Example:
Social or hidden population studies
"""

# =====================================================
# 5. MEASURES OF CENTRAL TENDENCY
# =====================================================

"""
Measures of central tendency describe
the center or typical value of a dataset.
"""

# Mean
"""
Mean (Average):
Sum of all values / Number of values

Example:
Data = [10, 20, 30]
Mean = (10 + 20 + 30) / 3 = 20
"""

# Median
"""
Median:
Middle value when data is sorted

Example:
Data = [10, 20, 30]
Median = 20
"""

# Mode
"""
Mode:
Value that occurs most frequently

Example:
Data = [2, 3, 3, 4]
Mode = 3
"""

# =====================================================
# 6. MEASURES OF DISPERSION
# =====================================================

"""
Measures of dispersion show how spread out
the data values are.
"""

# Range
"""
Range = Maximum value - Minimum value

Example:
Data = [10, 50]
Range = 40
"""

# Variance
"""
Variance measures the average squared deviation
from the mean.

Population Variance: σ²
Sample Variance: s²
"""

# Standard Deviation
"""
Standard Deviation is the square root of variance.

- Small SD: Data close to mean
- Large SD: Data widely spread
"""

# =====================================================
# INTERVIEW TIP
# =====================================================

"""
Interview Tip:
Central Tendency tells where the data is centered.
Dispersion tells how the data is spread.
"""

